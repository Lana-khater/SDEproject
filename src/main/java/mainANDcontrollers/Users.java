package mainANDcontrollers;

import java.sql.*;

public class Users {
    String name;
    int phone;
    String status;

    public Users(String name, int phone, String status) {
        this.name = name;
        this.phone = phone;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
